// Function to calculate carbon footprint based on selections
function calculateCarbonFootprint($people, $home_size, $food, $water, $household, $waste, $recycle, $recycling_categories, $personal_miles, $public_miles, $flight_distance) {
    $carbon_footprint = 0;
    $recycle = isset($_POST["recycle"]) ? $_POST["recycle"] : "";

    // Calculation based on selections
    switch ($people) {
        case 1:
            $carbon_footprint += 14;
            break;
        case 2:
            $carbon_footprint += 12;
            break;
        case 3:
            $carbon_footprint += 10;
            break;
        case 4:
            $carbon_footprint += 8;
            break;
        case 5:
            $carbon_footprint += 6;
            break;
        case 6:
            $carbon_footprint += 4;
            break;
        default:
            $carbon_footprint += 2;
            break;
    }

    switch ($home_size) {
        case "large":
            $carbon_footprint += 2;
            break;
        case "medium":
            $carbon_footprint += 4;
            break;
        case "small":
            $carbon_footprint += 7;
            break;
        case "apartment":
            $carbon_footprint += 10;
            break;
    }

    switch ($food) {
        case 1:
            $carbon_footprint += 10;
            break;
        case 2:
            $carbon_footprint += 8;
            break;
        case 3:
            $carbon_footprint += 4;
            break;
        case 4:
            $carbon_footprint += 2;
            break;
        case 5:
            $carbon_footprint += 12;
            break;
        case 6:
            $carbon_footprint += 6;
            break;
        case 7:
            $carbon_footprint += 2;
            break;
    }

    switch ($water) {
        case 1:
            $carbon_footprint += 1;
            break;
        case 2:
            $carbon_footprint += 2;
            break;
        case 3:
            $carbon_footprint += 3;
            break;
        case 4:
            break; // No points added if no dishwasher
        case 5:
            $carbon_footprint += 6; // Perform calculation twice if both dishwasher and washing machine
            break;
    }

    switch ($household) {
        case 1:
            $carbon_footprint += 10;
            break;
        case 2:
            $carbon_footprint += 8;
            break;
        case 3:
            $carbon_footprint += 6;
            break;
        case 4:
            $carbon_footprint += 4;
            break;
        case 5:
            $carbon_footprint += 2;
            break;
    }

    switch ($waste) {
        case 1:
            $carbon_footprint += 50;
            break;
        case 2:
            $carbon_footprint += 40;
            break;
        case 3:
            $carbon_footprint += 30;
            break;
        case 4:
            $carbon_footprint += 20;
            break;
        case 5:
            $carbon_footprint += 5;
            break;
    }

    switch ($recycling_categories) {
        case 1:
            $carbon_footprint -= 1;
            break;
        case 2:
            $carbon_footprint -= 2;
            break;
        case 3:
            $carbon_footprint -= 3;
            break;
        case 4:
            $carbon_footprint -= 4;
            break;
        case 5:
            $carbon_footprint -= 5;
            break;
        case 6:
            $carbon_footprint -= 6;
            break;
    }
    return $carbon_footprint;
}



